pub trait ThunkValue: Trace {
	type Output;
	fn get(self: Box<Self>) -> Result<Self::Output>;
}

#[derive(Trace)]
enum ThunkInner<T: Trace> {
	Computed(T),
	Errored(Error),
	Waiting(TraceBox<dyn ThunkValue<Output = T>>),
	Pending,
}

#[allow(clippy::module_name_repetitions)]
#[derive(Clone, Trace)]
pub struct Thunk<T: Trace>(Cc<RefCell<ThunkInner<T>>>);

impl<T> Thunk<T>
where
	T: Clone + Trace,
{
	pub fn new(f: TraceBox<dyn ThunkValue<Output = T>>) -> Self {
		Self(Cc::new(RefCell::new(ThunkInner::Waiting(f))))
	}
	pub fn evaluated(val: T) -> Self {
		Self(Cc::new(RefCell::new(ThunkInner::Computed(val))))
	}
	pub fn force(&self) -> Result<()> {
		self.evaluate()?;
		Ok(())
	}
	pub fn evaluate(&self) -> Result<T> {
		match &*self.0.borrow() {
			ThunkInner::Computed(v) => return Ok(v.clone()),
			ThunkInner::Errored(e) => return Err(e.clone()),
			ThunkInner::Pending => return Err(InfiniteRecursionDetected.into()),
			ThunkInner::Waiting(..) => (),
		};
		let ThunkInner::Waiting(value) = std::mem::replace(&mut *self.0.borrow_mut(), ThunkInner::Pending) else {
			unreachable!();
		};
		let new_value = match value.0.get() {
			Ok(v) => v,
			Err(e) => {
				*self.0.borrow_mut() = ThunkInner::Errored(e.clone());
				return Err(e);
			}
		};
		*self.0.borrow_mut() = ThunkInner::Computed(new_value.clone());
		Ok(new_value)
	}
}

type CacheKey = (Option<WeakObjValue>, Option<WeakObjValue>);

#[derive(Trace, Clone)]
pub struct CachedUnbound<I, T>
where
	I: Unbound<Bound = T>,
	T: Trace,
{
	cache: Cc<RefCell<GcHashMap<CacheKey, T>>>,
	value: I,
}
impl<I: Unbound<Bound = T>, T: Trace> CachedUnbound<I, T> {
	pub fn new(value: I) -> Self {
		Self {
			cache: Cc::new(RefCell::new(GcHashMap::new())),
			value,
		}
	}
}
impl<I: Unbound<Bound = T>, T: Clone + Trace> Unbound for CachedUnbound<I, T> {
	type Bound = T;
	fn bind(&self, sup: Option<ObjValue>, this: Option<ObjValue>) -> Result<T> {
		let cache_key = (
			sup.as_ref().map(|s| s.clone().downgrade()),
			this.as_ref().map(|t| t.clone().downgrade()),
		);
		{
			if let Some(t) = self.cache.borrow().get(&cache_key) {
				return Ok(t.clone());
			}
		}
		let bound = self.value.bind(sup, this)?;

		{
			let mut cache = self.cache.borrow_mut();
			cache.insert(cache_key, bound.clone());
		}

		Ok(bound)
	}
}

impl<T: Debug + Trace> Debug for Thunk<T> {
	fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
		write!(f, "Lazy")
	}
}
impl<T: Trace> PartialEq for Thunk<T> {
	fn eq(&self, other: &Self) -> bool {
		Cc::ptr_eq(&self.0, &other.0)
	}
}

#[derive(Debug, Clone, Trace)]
pub struct Slice {
	pub(crate) inner: ArrValue,
	pub(crate) from: u32,
	pub(crate) to: u32,
	pub(crate) step: u32,
}
impl Slice {
	const fn from(&self) -> usize {
		self.from as usize
	}
	const fn to(&self) -> usize {
		self.to as usize
	}
	const fn step(&self) -> usize {
		self.step as usize
	}
	const fn len(&self) -> usize {
		// TODO: use div_ceil
		let diff = self.to() - self.from();
		let rem = diff % self.step();
		let div = diff / self.step();

		if rem == 0 {
			div
		} else {
			div + 1
		}
	}
}

/// Represents a Jsonnet array value.
#[derive(Debug, Clone, Trace)]
// may contrain other ArrValue
#[trace(tracking(force))]
pub enum ArrValue {
	/// Layout optimized byte array.
	Bytes(#[trace(skip)] IBytes),
	/// Every element is lazy evaluated.
	Lazy(Cc<Vec<Thunk<Val>>>),
	/// Every field is already evaluated.
	Eager(Cc<Vec<Val>>),
	/// Concatenation of two arrays of any kind.
	Extended(Box<(Self, Self)>),
	/// Represents a integer array in form `[start, start + 1, ... end - 1, end]`.
	/// This kind of arrays is generated by `std.range(start, end)` call, and used for loops.
	Range(i32, i32),
	/// Sliced array view.
	Slice(Box<Slice>),
	/// Reversed array view.
	/// Returned by `std.reverse(other)` call
	Reversed(Box<Self>),
}

#[cfg(target_pointer_width = "64")]
static_assertions::assert_eq_size!(ArrValue, [u8; 16]);

impl ArrValue {
	pub fn new_eager() -> Self {
		Self::Eager(Cc::new(Vec::new()))
	}
	pub fn empty() -> Self {
		Self::new_range(0, 0)
	}

	/// # Panics
	/// If a > b
	#[inline]
	pub fn new_range(a: i32, b: i32) -> Self {
		assert!(a <= b);
		Self::Range(a, b)
	}

	/// # Panics
	/// If passed numbers are incorrect
	#[must_use]
	pub fn slice(self, from: Option<usize>, to: Option<usize>, step: Option<usize>) -> Self {
		let len = self.len();
		let from = from.unwrap_or(0);
		let to = to.unwrap_or(len).min(len);
		let step = step.unwrap_or(1);
		assert!(from < to);
		assert!(step > 0);

		Self::Slice(Box::new(Slice {
			inner: self,
			from: from as u32,
			to: to as u32,
			step: step as u32,
		}))
	}

	/// Array length.
	pub fn len(&self) -> usize {
		match self {
			Self::Bytes(i) => i.len(),
			Self::Lazy(l) => l.len(),
			Self::Eager(e) => e.len(),
			Self::Extended(v) => v.0.len() + v.1.len(),
			Self::Range(a, b) => a.abs_diff(*b) as usize + 1,
			Self::Reversed(i) => i.len(),
			Self::Slice(s) => s.len(),
		}
	}

	/// Is array contains no elements?
	pub fn is_empty(&self) -> bool {
		self.len() == 0
	}

	/// Get array element by index, evaluating it, if it is lazy.
	///
	/// Returns `None` on out-of-bounds condition.
	pub fn get(&self, index: usize) -> Result<Option<Val>> {
		match self {
			Self::Bytes(i) => i
				.get(index)
				.map_or(Ok(None), |v| Ok(Some(Val::Num(f64::from(*v))))),
			Self::Lazy(vec) => {
				if let Some(v) = vec.get(index) {
					Ok(Some(v.evaluate()?))
				} else {
					Ok(None)
				}
			}
			Self::Eager(vec) => Ok(vec.get(index).cloned()),
			Self::Extended(v) => {
				let a_len = v.0.len();
				if a_len > index {
					v.0.get(index)
				} else {
					v.1.get(index - a_len)
				}
			}
			Self::Range(a, _) => {
				if index >= self.len() {
					return Ok(None);
				}
				Ok(Some(Val::Num(((*a as isize) + index as isize) as f64)))
			}
			Self::Reversed(v) => {
				let len = v.len();
				if index >= len {
					return Ok(None);
				}
				v.get(len - index - 1)
			}
			Self::Slice(v) => {
				let index = v.from() + index * v.step();
				if index >= v.to() {
					return Ok(None);
				}
				v.inner.get(index)
			}
		}
	}

	/// Get array element by index, without evaluation.
	///
	/// Returns `None` on out-of-bounds condition.
	pub fn get_lazy(&self, index: usize) -> Option<Thunk<Val>> {
		match self {
			Self::Bytes(i) => i
				.get(index)
				.map(|b| Thunk::evaluated(Val::Num(f64::from(*b)))),
			Self::Lazy(vec) => vec.get(index).cloned(),
			Self::Eager(vec) => vec.get(index).cloned().map(Thunk::evaluated),
			Self::Extended(v) => {
				let a_len = v.0.len();
				if a_len > index {
					v.0.get_lazy(index)
				} else {
					v.1.get_lazy(index - a_len)
				}
			}
			Self::Range(a, _) => {
				if index >= self.len() {
					return None;
				}
				Some(Thunk::evaluated(Val::Num(
					((*a as isize) + index as isize) as f64,
				)))
			}
			Self::Reversed(v) => {
				let len = v.len();
				if index >= len {
					return None;
				}
				v.get_lazy(len - index - 1)
			}
			Self::Slice(s) => {
				let index = s.from() + index * s.step();
				if index >= s.to() {
					return None;
				}
				s.inner.get_lazy(index)
			}
		}
	}

	/// Evaluate all array elements, returning new array.
	pub fn evaluated(&self) -> Result<Cc<Vec<Val>>> {
		Ok(match self {
			Self::Bytes(i) => {
				let mut out = Vec::with_capacity(i.len());
				for v in i.iter() {
					out.push(Val::Num(f64::from(*v)));
				}
				Cc::new(out)
			}
			Self::Lazy(vec) => {
				let mut out = Vec::with_capacity(vec.len());
				for item in vec.iter() {
					out.push(item.evaluate()?);
				}
				Cc::new(out)
			}
			Self::Eager(vec) => vec.clone(),
			Self::Extended(_v) => {
				let mut out = Vec::with_capacity(self.len());
				for item in self.iter() {
					out.push(item?);
				}
				Cc::new(out)
			}
			Self::Range(a, b) => {
				let mut out = Vec::with_capacity(self.len());
				for i in *a..=*b {
					out.push(Val::Num(f64::from(i)));
				}
				Cc::new(out)
			}
			Self::Reversed(r) => {
				let mut r = r.evaluated()?;
				Cc::update_with(&mut r, |v| v.reverse());
				r
			}
			Self::Slice(v) => {
				let mut out = Vec::with_capacity(v.inner.len());
				for v in v
					.inner
					.iter_lazy()
					.skip(v.from())
					.take(v.to() - v.from())
					.step_by(v.step())
				{
					out.push(v.evaluate()?);
				}
				Cc::new(out)
			}
		})
	}

	/// Iterate over elements, evaluating them.
	pub fn iter(&self) -> impl DoubleEndedIterator<Item = Result<Val>> + '_ {
		(0..self.len()).map(move |idx| match self {
			Self::Bytes(b) => Ok(Val::Num(f64::from(b[idx]))),
			Self::Lazy(l) => l[idx].evaluate(),
			Self::Eager(e) => Ok(e[idx].clone()),
			Self::Extended(..) | Self::Range(..) | Self::Reversed(..) | Self::Slice(..) => {
				self.get(idx).map(|e| e.expect("idx < len"))
			}
		})
	}

	/// Iterate over elements, returning lazy values.
	pub fn iter_lazy(&self) -> impl DoubleEndedIterator<Item = Thunk<Val>> + '_ {
		(0..self.len()).map(move |idx| match self {
			Self::Bytes(b) => Thunk::evaluated(Val::Num(f64::from(b[idx]))),
			Self::Lazy(l) => l[idx].clone(),
			Self::Eager(e) => Thunk::evaluated(e[idx].clone()),
			Self::Slice(..) | Self::Extended(..) | Self::Range(..) | Self::Reversed(..) => {
				self.get_lazy(idx).expect("idx < len")
			}
		})
	}

	/// Return a reversed view on current array.
	#[must_use]
	pub fn reversed(self) -> Self {
		Self::Reversed(Box::new(self))
	}

	/// Return a new array, produced by passing every element of current array to specified callback function.
	pub fn map(self, mapper: impl Fn(Val) -> Result<Val>) -> Result<Self> {
		let mut out = Vec::with_capacity(self.len());

		for value in self.iter() {
			out.push(mapper(value?)?);
		}

		Ok(Self::Eager(Cc::new(out)))
	}

	/// Return a new array, produced from current array by removing every value, for which specified callback function returns false.
	pub fn filter(self, filter: impl Fn(&Val) -> Result<bool>) -> Result<Self> {
		let mut out = Vec::with_capacity(self.len());

		for value in self.iter() {
			let value = value?;
			if filter(&value)? {
				out.push(value);
			}
		}

		Ok(Self::Eager(Cc::new(out)))
	}

	pub fn ptr_eq(a: &Self, b: &Self) -> bool {
		match (a, b) {
			(Self::Lazy(a), Self::Lazy(b)) => Cc::ptr_eq(a, b),
			(Self::Eager(a), Self::Eager(b)) => Cc::ptr_eq(a, b),
			_ => false,
		}
	}
}

impl From<Vec<Thunk<Val>>> for ArrValue {
	fn from(v: Vec<Thunk<Val>>) -> Self {
		Self::Lazy(Cc::new(v))
	}
}

impl From<Vec<Val>> for ArrValue {
	fn from(v: Vec<Val>) -> Self {
		Self::Eager(Cc::new(v))
	}
}

/// Represents a Jsonnet value, which can be spliced or indexed (string or array).
#[allow(clippy::module_name_repetitions)]
pub enum IndexableVal {
	/// String.
	Str(IStr),
	/// Array.
	Arr(ArrValue),
}
impl IndexableVal {
	/// Slice the value.
	///
	/// # Implementation
	///
	/// For strings, will create a copy of specified interval.
	///
	/// For arrays, nothing will be copied on this call, instead [`ArrValue::Slice`] view will be returned.
	pub fn slice(
		self,
		index: Option<BoundedUsize<0, { i32::MAX as usize }>>,
		end: Option<BoundedUsize<0, { i32::MAX as usize }>>,
		step: Option<BoundedUsize<1, { i32::MAX as usize }>>,
	) -> Result<Self> {
		match &self {
			IndexableVal::Str(s) => {
				let index = index.as_deref().copied().unwrap_or(0);
				let end = end.as_deref().copied().unwrap_or(usize::MAX);
				let step = step.as_deref().copied().unwrap_or(1);

				if index >= end {
					return Ok(Self::Str("".into()));
				}

				Ok(Self::Str(
					(s.chars()
						.skip(index)
						.take(end - index)
						.step_by(step)
						.collect::<String>())
					.into(),
				))
			}
			IndexableVal::Arr(arr) => {
				let index = index.as_deref().copied().unwrap_or(0);
				let end = end.as_deref().copied().unwrap_or(usize::MAX).min(arr.len());
				let step = step.as_deref().copied().unwrap_or(1);

				if index >= end {
					return Ok(Self::Arr(ArrValue::new_eager()));
				}

				Ok(Self::Arr(ArrValue::Slice(Box::new(Slice {
					inner: arr.clone(),
					from: index as u32,
					to: end as u32,
					step: step as u32,
				}))))
			}
		}
	}
}

/// Represents any valid Jsonnet value.
#[derive(Debug, Clone, Trace)]
pub enum Val {
	/// Represents a Jsonnet boolean.
	Bool(bool),
	/// Represents a Jsonnet null value.
	Null,
	/// Represents a Jsonnet string.
	Str(IStr),
	/// Represents a Jsonnet number.
	/// Should be finite, and not NaN
	/// This restriction isn't enforced by enum, as enum field can't be marked as private
	Num(f64),
	/// Represents a Jsonnet array.
	Arr(ArrValue),
	/// Represents a Jsonnet object.
	Obj(ObjValue),
	/// Represents a Jsonnet function.
	Func(FuncVal),
}

impl From<IndexableVal> for Val {
	fn from(v: IndexableVal) -> Self {
		match v {
			IndexableVal::Str(s) => Self::Str(s),
			IndexableVal::Arr(a) => Self::Arr(a),
		}
	}
}

// Broken between stable and nightly, as there is new layout size optimization
// #[cfg(target_pointer_width = "64")]
